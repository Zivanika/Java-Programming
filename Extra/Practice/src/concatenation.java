import java.util.*;

class concatenation {
    public static void main(String args[]) {
        Scanner Sc = new Scanner(System.in);
        System.out.print("Enter Your First Name: ");
        String fn = Sc.next();
        System.out.print("Enter Your Last Name: ");
        String ln = Sc.next();
        System.out.println(ln+" "+fn);
    }
}